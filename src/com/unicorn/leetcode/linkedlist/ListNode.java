package com.unicorn.leetcode.linkedlist;

public class ListNode {

    int val;
    ListNode next;
    ListNode() {}
    ListNode(int val) { this.val = val; }
    ListNode(int val, ListNode next) { this.val = val; this.next = next; }
}

 class Node {

    int val;
    Node next;
    Node random;
    Node() {}
    Node(int val) { this.val = val; }

}
